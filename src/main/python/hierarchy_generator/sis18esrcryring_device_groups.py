from abc import ABCMeta, abstractproperty

from beamdiagnostic_device_groups import BeamdiagnosticDeviceGroups

class SIS18ESRCRYRINGDeviceGroups(BeamdiagnosticDeviceGroups):
  __metaclass__ = ABCMeta

    
  def __init__(self):
    pass

  @abstractproperty
  def old_cavities_ampl(self):
    pass

  @abstractproperty
  def old_cavities_freq(self):
    pass

  @abstractproperty
  def old_cavities_phase(self):
    pass

  @abstractproperty
  def horizontal_correction_coils(self):
    pass

  @abstractproperty
  def extraction_bump_correction_coils(self):
    pass

  @abstractproperty
  def electrostatic_quadrupole(self):
    pass

  @abstractproperty
  def chopper(self):
    pass
